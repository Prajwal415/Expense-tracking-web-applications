function applyTranslations(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS['en'];
  
  // Update text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      // Handle inputs with placeholders vs regular text
      if (el.tagName === 'INPUT' && el.getAttribute('placeholder')) {
        el.placeholder = t[key];
      } else {
        // Preserve icons if they exist in a separate span, otherwise replace text
        // Simple replacement for now:
        if (el.children.length === 0) {
            el.textContent = t[key];
        } else {
            // If element has children (like icons), try to find a text node or specific span
            // For this implementation, we assume data-i18n is on the text container itself
            el.childNodes.forEach(node => {
                if (node.nodeType === Node.TEXT_NODE && node.textContent.trim() !== '') {
                    node.textContent = t[key];
                }
            });
        }
      }
    }
  });

  // Update document language
  document.documentElement.lang = lang;
}

function initTranslations() {
  // 1. Try to get from localStorage (fastest)
  const savedLang = localStorage.getItem('appLanguage') || 'en';
  applyTranslations(savedLang);
}

